- 정확도를 높이면 → 필터버블 심화
- 다양성을 높이면 → 추천 만족도 하락

기존 접근법은 랜덤하게 다른 콘텐츠를 섞는 정도인데, 이는 사용자 경험을 해칩니다.

## DeepSight의 접근: 다관점 추천

DeepSight는 필터버블 문제를 ''다양한 관점의 페르소나''로 해결합니다.

### 1. 다양한 성향의 페르소나 풀
사용자와 벡터가 완전히 같은 페르소나만 매칭하지 않습니다. 일부 차원에서는 비슷하지만 다른 차원에서는 다른 성향의 페르소나도 함께 추천하여, 새로운 관점을 자연스럽게 소개합니다.

### 2. 다양성 팩터(Diversity Factor)
매칭 알고리즘에 다양성 팩터를 포함하여, 추천 결과가 한쪽으로 쏠리지 않도록 조절합니다. 이는 Engine Studio에서 알고리즘 튜닝을 통해 최적의 균형점을 찾습니다.

### 3. 설명 기반 탐색
페르소나가 "나와 다른 관점에서 이 콘텐츠를 추천합니다"라고 설명하면, 사용자는 거부감 없이 새로운 콘텐츠를 시도해볼 수 있습니다. 무작위 삽입이 아니라 이유가 있는 다양성입니다.

