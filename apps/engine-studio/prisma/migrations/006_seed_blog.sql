- 짧은 시간 내 전체 6D 프로필 생성 가능
- 사용자가 직접 참여하므로 정확도가 높음
- 확신도(Confidence)도 함께 계산

### 2. SNS 연동 방식 (Social Analysis)

사용자의 소셜 미디어 데이터를 분석하여 6D 벡터를 추론합니다. 질문에 답하지 않아도 프로필을 생성할 수 있어 온보딩 마찰을 최소화합니다.

**분석 대상**: 팔로우하는 계정의 유형, 좋아요/공유한 콘텐츠의 패턴, 관심사 태그 등

**특징**:
- 사용자 동의 기반으로만 진행
- 원시 데이터 저장 없이 벡터값만 산출
- 즉시 프로필 생성 가능

### 3. 문답 + SNS 병행 (Hybrid)

