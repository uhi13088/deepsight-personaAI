## 필터버블이란?

필터버블(Filter Bubble)은 추천 알고리즘이 사용자의 기존 선호에만 맞춰 콘텐츠를 추천하면서, 사용자가 점점 좁은 범위의 콘텐츠만 접하게 되는 현상입니다.

유튜브에서 고양이 영상을 몇 개 보면 피드가 고양이로 가득 차는 경험, 뉴스 앱에서 특정 관점의 기사만 계속 보이는 경험이 대표적입니다.

## 왜 문제인가?

- **콘텐츠 피로도 증가**: 비슷한 콘텐츠의 반복은 흥미를 떨어뜨립니다
- **새로운 발견 차단**: 좋아할 수 있었을 콘텐츠를 만나지 못합니다
- **편향 강화**: 특히 뉴스/정보 분야에서 한쪽 관점만 접하게 됩니다
- **장기 이탈**: 결국 "추천이 재미없다"는 이유로 서비스를 떠납니다

## 기존 시스템의 딜레마

