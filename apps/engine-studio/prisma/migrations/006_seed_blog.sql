## 6개 차원의 의미

DeepSight의 6D 벡터는 콘텐츠 소비 성향을 6개의 독립적인 축으로 분석합니다. 각 차원은 0.0에서 1.0 사이의 연속적인 값을 가집니다.

### Depth (분석 깊이) — 직관적 ↔ 심층적
콘텐츠를 얼마나 깊이 파고들어 분석하는지를 나타냅니다. 0.0에 가까운 사용자는 직감적으로 "재밌다/별로다"를 판단하고, 1.0에 가까운 사용자는 작품의 구조, 의도, 맥락을 분석합니다.

### Lens (판단 렌즈) — 감성적 ↔ 논리적
콘텐츠를 평가할 때의 관점입니다. 감성적 사용자는 "마음이 동했는지"를 기준으로, 논리적 사용자는 "객관적으로 잘 만들어졌는지"를 기준으로 평가합니다.

### Stance (평가 태도) — 수용적 ↔ 비판적
새로운 콘텐츠를 접할 때의 기본 태도입니다. 수용적 사용자는 열린 마음으로 장점을 찾고, 비판적 사용자는 아쉬운 점을 먼저 분석합니다.

### Scope (관심 범위) — 핵심만 ↔ 디테일
정보 소비의 폭입니다. 핵심만 원하는 사용자에게는 요약된 정보를, 디테일을 원하는 사용자에게는 풍부한 분석을 제공합니다.

### Taste (취향 성향) — 클래식 ↔ 실험적
콘텐츠 선택의 모험 정도입니다. 클래식 성향은 검증된 명작을, 실험적 성향은 새롭고 독특한 작품을 선호합니다.

### Purpose (소비 목적) — 오락 ↔ 의미추구
콘텐츠를 소비하는 궁극적 목적입니다. 순수한 즐거움을 위한 것인지, 의미와 메시지를 찾는 것인지를 나타냅니다.

